  function captureLatestIncoming() {
    const candidates = [
      '[data-testid="chat-list"]',
      '[class*="ChatList"]',
      '[class*="messageList"]',
      '[class*="MessageList"]',
      '[role="log"]',
    ];
    let container = null;
    for (const sel of candidates) {
      const el = document.querySelector(sel);
      if (el && el.innerText && el.innerText.length > 50) {
        container = el;
        break;
      }
    }
    if (!container) {
      const all = document.querySelectorAll("div");
      let best = null;
      let bestLen = 0;
      for (const el of all) {
        const style = getComputedStyle(el);
        if (
          (style.overflowY === "auto" || style.overflowY === "scroll") &&
          el.innerText &&
          el.innerText.length > bestLen
        ) {
          bestLen = el.innerText.length;
          best = el;
        }
      }
      container = best;
    }
    if (!container) return "";

    const lines = container.innerText
      .split("\n")
      .map((l) => l.trim())
      .filter((l) => l.length > 0);
    return lines.slice(-3).join("\n");
  }

  function readMessageBoxContent() {
    const input = findMessageBox();
    if (!input) return "";
    if (input.tagName === "TEXTAREA" || input.tagName === "INPUT") {
      return input.value || "";
    }
    return input.innerText || "";
  }

  function findMessageBox() {
    const candidates = [
      'textarea[placeholder*="メッセージ"]',
      'textarea[placeholder*="入力"]',
      'div[contenteditable="true"]',
      "textarea",
    ];
    for (const sel of candidates) {
      const els = document.querySelectorAll(sel);
      for (const el of els) {
        if (el.offsetParent === null) continue;
        return el;
      }
    }
    return null;
  }

